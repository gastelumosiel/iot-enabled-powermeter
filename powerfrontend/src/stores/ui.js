export const useUiStore = defineStore('ui', {
  state: () => ({
    theme: localStorage.getItem('powerlytix_theme') || 'light',
    language: localStorage.getItem('powerlytix_language') || 'ES',
  }),
  getters: {
    t: (state) => (key) => messages[state.language]?.[key] || messages.ES[key] || key,
  },
  actions: {
    setTheme(theme) {
      this.theme = theme
      localStorage.setItem('powerlytix_theme', theme)
      document.documentElement.dataset.theme = theme
    },
    toggleTheme() {
      this.setTheme(this.theme === 'light' ? 'dark' : 'light')
    },
    setLanguage(language) {
      this.language = language
      localStorage.setItem('powerlytix_language', language)
    },
    hydrate() {
      document.documentElement.dataset.theme = this.theme
    },
  },
})
